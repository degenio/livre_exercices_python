var1 = 20
var2 = 8
div = var1 / var2
reste = var1 % var2
div_entiere = var1 // var2

print('La division de {} par {} donne:{}'.format(var1, var2, div))
print('Le reste de la division de {} par {} donne:{}'.format(var1, var2, reste))
#La division entière donne la valeur entiere du résultat seulement
print('La division entiere de {} par {} donne:{}'.format(var1, var2, div_entiere))
